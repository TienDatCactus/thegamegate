-- Add randomized numbers to languageId
WITH NumberedRows AS (
    SELECT ROW_NUMBER() OVER (ORDER BY (SELECT NULL)) AS RowNum
    FROM Games
)
UPDATE Games
SET LanguageId = ISNULL(LanguageId, (NumberedRows.RowNum % 10) + 1)
FROM Games
JOIN NumberedRows ON Games.CategoryID = NumberedRows.RowNum;


